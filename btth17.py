a = int(input(" Nhâp số nguyên a = "))
b = int(input(" Nhâp số nguyên b = "))
c = int(input(" Nhâp số nguyên c = "))
so_lon_nhat = max(a, b, c)
print("Số nguyên lớn nhất trong 3 số a,b,c là: ", so_lon_nhat)
so_nho_nhat = min(a, b, c)
print("Số nguyên nhỏ nhất trong 3 số a,b,c là: ", so_nho_nhat)
